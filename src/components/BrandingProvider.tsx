import React, { useLayoutEffect } from 'react';
import { useOrders } from '@/contexts/OrderContext';

export function BrandingProvider({ children }: { children: React.ReactNode }) {
  const { settings } = useOrders();

  useLayoutEffect(() => {
    if (settings.primaryColor) {
      // Convert hex to HSL for shadcn compatibility if possible, 
      // but for simplicity we'll just set the hex directly or as a CSS variable that 
      // tailwind can use if we override the theme.
      // Shadcn uses HSL strings like "0 84% 45%".
      
      const hexToHsl = (hex: string) => {
        let r = 0, g = 0, b = 0;
        if (hex.length === 4) {
          r = parseInt(hex[1] + hex[1], 16);
          g = parseInt(hex[2] + hex[2], 16);
          b = parseInt(hex[3] + hex[3], 16);
        } else if (hex.length === 7) {
          r = parseInt(hex.substring(1, 3), 16);
          g = parseInt(hex.substring(3, 5), 16);
          b = parseInt(hex.substring(5, 7), 16);
        }
        r /= 255; g /= 255; b /= 255;
        const max = Math.max(r, g, b), min = Math.min(r, g, b);
        let h = 0, s, l = (max + min) / 2;
        if (max === min) {
          h = s = 0;
        } else {
          const d = max - min;
          s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
          switch (max) {
            case r: h = (g - b) / d + (g < b ? 6 : 0); break;
            case g: h = (b - r) / d + 2; break;
            case b: h = (r - g) / d + 4; break;
          }
          h /= 6;
        }
        return `${Math.round(h * 360)} ${Math.round(s * 100)}% ${Math.round(l * 100)}%`;
      };

      try {
        const hsl = hexToHsl(settings.primaryColor);
        document.documentElement.style.setProperty('--primary', hsl);
        document.documentElement.style.setProperty('--ring', hsl);
      } catch (e) {
        console.error("Invalid color:", settings.primaryColor);
      }
    }
    
    // Update Title and Favicon if logo exists
    if (settings.name) {
      document.title = settings.name;
    }
  }, [settings.primaryColor, settings.name]);

  return <>{children}</>;
}
